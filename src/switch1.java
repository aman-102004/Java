public class switch1 {
    public static void main(String[] args) {
        int a=1;
        switch(a){
            case 1:
                System.out.println("Byeeee");
                break;

            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("Hiiiii");
        }
    }
}
