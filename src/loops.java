public class loops {
    public static void main(String[] args) {
        System.out.println("For loop");
        for(int i=1;i<=5;i++){
            System.out.println((i)+" Hello");
        }
        int a=1;
        System.out.println("While loop");
        while(a<5){
            System.out.println((a)+" Hello");
            a++;
        }
    }
}
